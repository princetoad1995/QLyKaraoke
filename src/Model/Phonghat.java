package Model;

public class Phonghat {
    private int maPhonghat;
    private String tenPhong;
    private int giaPhong;
    private String coPhong;
    private String hangPhong;
    private char trangthai;

    public Phonghat() {
    }

    public int getMaPhonghat() {
        return maPhonghat;
    }

    public void setMaPhonghat(int maPhonghat) {
        this.maPhonghat = maPhonghat;
    }

    public String getTenPhong() {
        return tenPhong;
    }

    public void setTenPhong(String tenPhong) {
        this.tenPhong = tenPhong;
    }

    public int getGiaPhong() {
        return giaPhong;
    }

    public void setGiaPhong(int giaPhong) {
        this.giaPhong = giaPhong;
    }

    public String getCoPhong() {
        return coPhong;
    }

    public void setCoPhong(String coPhong) {
        this.coPhong = coPhong;
    }

    public String getHangPhong() {
        return hangPhong;
    }

    public void setHangPhong(String hangPhong) {
        this.hangPhong = hangPhong;
    }

    public char getTrangthai() {
        return trangthai;
    }

    public void setTrangthai(char trangthai) {
        this.trangthai = trangthai;
    }
    
    
}
